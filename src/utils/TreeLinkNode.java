public class TreeLinkNode {
    public int val;
    public TreeLinkNode left;
    public TreeLinkNode right;
    public TreeLinkNode next;

    public TreeLinkNode(int value) {
        this.val = value;
        this.left = null;
        this.right = null;
        this.next = null;
    }
}
